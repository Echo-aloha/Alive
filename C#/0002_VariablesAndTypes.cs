using System;

class Program
{
    static void Main(string[] args)
    {
        int age = 25;
        double height = 1.75;
        string name = "Alice";
        bool isStudent = true;

        Console.WriteLine("Name: " + name);
        Console.WriteLine("Age: " + age);
        Console.WriteLine("Height: " + height + " meters");
        Console.WriteLine("Is a student: " + isStudent);
    }
}
